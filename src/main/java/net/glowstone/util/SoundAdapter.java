    SoundAdapter(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }
    
    public static Sound fromId(String id) {
        if (!id.startsWith("minecraft:")) {
            id = "minecraft:" + id;
        }
        for (SoundAdapter soundAdapter : values()) {
            if (soundAdapter.getId().equalsIgnoreCase(id)) {
                return Sound.valueOf(soundAdapter.name());
            }
        }
        return null;
    }

    public static String fromSound(Sound sound) {
        for (SoundAdapter soundAdapter : values()) {
            if (soundAdapter.name().equals(sound.name())) {
                return soundAdapter.getId();
            }
        }
        return null;
    }
